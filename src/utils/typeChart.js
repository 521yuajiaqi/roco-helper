/**
 * 计算攻击方对防守方的属性克制倍数
 */
export function calcMultiplier(atkElement, atkSubElement, defElement, defSubElement) {
  let multiplier = 1.0
  multiplier *= getSingleMultiplier(atkElement, defElement)
  if (defSubElement) {
    multiplier *= getSingleMultiplier(atkElement, defSubElement)
  }
  return Math.round(multiplier * 100) / 100
}

function getSingleMultiplier(atk, def) {
  if (!TYPE_CHART[atk] || !TYPE_CHART[atk][def]) return 1.0
  return TYPE_CHART[atk][def]
}

export function getTypeRelations(element, subElement) {
  return ELEMENTS.map(el => {
    let multiplier = getSingleMultiplier(el, element)
    if (subElement) {
      multiplier *= getSingleMultiplier(el, subElement)
    }
    return { element: el, multiplier }
  })
}

export function describeMultiplier(multiplier) {
  if (multiplier >= 4) return '四倍克制，伤害极高！'
  if (multiplier >= 2) return '克制，伤害翻倍'
  if (multiplier >= 1.5) return '略微克制'
  if (multiplier === 1) return '正常伤害'
  if (multiplier >= 0.5) return '抵抗，伤害减半'
  if (multiplier > 0) return '强力抵抗'
  return '免疫！'
}
